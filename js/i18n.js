let currentLang = 'it';

function t(key) {
  return (I18N[currentLang] && I18N[currentLang][key]) || key;
}

function tLang(key, lang) {
  return (I18N[lang] && I18N[lang][key]) || key;
}

function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val !== key) el.textContent = val;
  });
}

async function switchLang(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;
  document.getElementById('btnLangIt').classList.toggle('active', lang === 'it');
  document.getElementById('btnLangEn').classList.toggle('active', lang === 'en');
  applyI18n();
  document.querySelectorAll('.auto-trans-btn').forEach(btn => {
    btn.style.display = btn.dataset.fromLang === lang ? '' : 'none';
  });
  if (typeof renderAll === 'function') await renderAll();
}

function bootSelect(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;
  document.getElementById('bootLang').style.display = 'none';
  document.getElementById('btnLangIt').classList.toggle('active', lang === 'it');
  document.getElementById('btnLangEn').classList.toggle('active', lang === 'en');
  applyI18n();
  showFolderSentinel();
}
